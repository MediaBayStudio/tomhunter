      <h3 class="complex-of-service-item__title title-underline">Этапы проведения</h3>

      <p class="complex-of-service-item__text">
      1.	Детально анализируем соответствие положениям GDPR с учётом выполненных требований по защите ПДн.<br>
      2.	Проводим обработку ПДн в соответствие GDPR:<br>
      — формируем реестр процессов обработки ПДн<br>
      — оцениваем риски безопасности ПДн<br>
      — разрабатываем перечень организационно-технических мероприятий по приведению порядка обработки и защиты ПДн в соответствие GDPR<br>
      — выстраиваем процессы обеспечения безопасности ПДн<br>
      — разрабатываем внутренние нормативные и организационно-распорядительные документы по порядку обработки и защиты ПДн<br>
      3.	Создаём систему технической защиты ПДн<br>
      4.	Повышаем осведомлённость персонала по вопросам обработки и защиты ПДн
      </p>
